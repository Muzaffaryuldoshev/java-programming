package loops;

public class SquareOfNums {
    public static void main(String[] args) {
        System.out.println("Number | Square of Number");
        System.out.println("--------------------------");

        for (int i = 1; i <= 10; i++) {
            System.out.println(i + "    |" + "      " + i * i);
        }

    }
}
